#include<iostream>
using namespace std;

int main(){
    cout<<"Size if integer: "<<sizeof(int)<<endl;
    cout<<"Size if signed integer: "<<sizeof(signed int)<<endl;
    cout<<"Size if unsigned integer: "<<sizeof(unsigned int)<<endl;
    cout<<"Size if char: "<<sizeof(char)<<endl;
    cout<<"Size if long long: "<<sizeof(long long)<<endl;
    cout<<"Size if float: "<<sizeof(float)<<endl;
    cout<<"Size if double: "<<sizeof(double)<<endl;
    cout<<"Size if bool: "<<sizeof(bool)<<endl;
    cout<<"Size if long double: "<<sizeof(long double)<<endl;
    cout<<"Size if wchar_t: "<<sizeof(wchar_t)<<endl;
    cout<<"Size if signed char: "<<sizeof(signed char)<<endl;
    cout<<"Size if unsigned char: "<<sizeof(unsigned char)<<endl;
    return 0;
}